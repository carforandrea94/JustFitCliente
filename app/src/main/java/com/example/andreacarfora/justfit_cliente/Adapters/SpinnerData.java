package com.example.andreacarfora.justfit_cliente.Adapters;

public class SpinnerData {
    private String iconName;
    private int icon;

    public SpinnerData(String iconName, int icon) {
        this.iconName = iconName;
        this.icon = icon;
    }

    public String getIconName() {
        return iconName;
    }

    public int getIcon() {
        return icon;
    }

}
